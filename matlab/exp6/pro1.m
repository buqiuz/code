% 您已有的数据
t = (1900:1:1926)'; % 年份
Q = [1.05, 1.18, 1.29, 1.30, 1.30, 1.42, 1.50, 1.52, 1.46, 1.60,...
1.69, 1.81, 1.93, 1.95, 2.01, 2.00, 2.09, 1.96, 2.20, 2.12,...
2.16, 2.08, 2.24, 2.56, 2.34, 2.45, 2.58];
K = [1.04, 1.06, 1.16, 1.22, 1.27, 1.37, 1.44, 1.53, 1.57, 2.05,...
2.51, 2.63, 2.74, 2.82, 3.24, 3.24, 3.61, 4.10, 4.36, 4.77,...
4.75, 4.54, 4.54, 4.58, 4.58, 4.58, 4.54];
L = [1.05, 1.08, 1.18, 1.22, 1.17, 1.30, 1.39, 1.47, 1.31, 1.43,...
1.58, 1.59, 1.66, 1.68, 1.65, 1.62, 1.86, 1.93, 1.96, 1.95,...
1.90, 1.58, 1.67, 1.82, 1.60, 1.61, 1.64];

% 正确组织自变量数据
X = [K; L]; % 每一列代表一个自变量，每一行是一次观测

% Cobb-Douglas 生产函数的非线性形式，包括 a 参数
cobbDouglas = @(b, x) b(1) * x(1,:).^b(2) .* x(2,:).^b(3);

% 初始参数猜测、上下界限保持不变
beta0 = [0.5, 0.5, 0.5];
lb = [0, 0, 0];
ub = [Inf, 1, 1];

% 使用非线性最小二乘方法拟合模型
% 注意：'lsqcurvefit' 需要 Optimization Toolbox
% 如果您没有这个工具箱，可能需要使用其他方法
beta = lsqcurvefit(cobbDouglas, beta0, X, Q, lb, ub);

% 打印结果
a_est = beta(1);
alpha_est = beta(2);
beta_est = beta(3);
fprintf('Estimated a: %f\n', a_est);
fprintf('Estimated alpha: %f\n', alpha_est);
fprintf('Estimated beta: %f\n', beta_est);
