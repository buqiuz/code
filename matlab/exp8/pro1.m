t = 2011:2022;
p = [29.44,29.75,30.11,30.43,30.70,31.10,31.44,31.63,31.88,32.05,32.12,32.13];
y = log(p);  %求ln(p)函数值
a = polyfit(t,y,1)  %用一次多项式对t和y进行拟合
z = polyval(a,t);  %求得以a为系数的多项式在t处的函数值
z1 = exp(z)
r = a(1)
plot(t,p,'bo',t,z1,'r')  %分别画出散点图以及拟合曲线图
xlabel('时间');
ylabel('人口数量');
legend('实际数据','理论曲线');
